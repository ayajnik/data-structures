person_1 = {'name': 'Ayush', 'age': 29}
print(person_1)
## now if I want to change the vae of `age` in the above dictionary, we will just use the indexing principle and call the key `age` and assign the new value

person_1['age'] = 30
print(person_1)

## now if i want to insert a new key to the dictionary then we will use the indexing principle, add a new index called `location` and then assign a value.

person_1['Location'] = 'New Delhi/New York'
print(person_1)
